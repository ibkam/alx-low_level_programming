#include <stdio.h>

/**
 * add - a function that returns the sum of two numbers
 * @num1: the first number
 * @num2: the second number
 * Return: the sum of the numbers
 */

int add(int num1, int num2)
{
	return (num1 + num2);
}
/**
 * sub - a function that returns the difference of two numbers
 * @num1: the first number
 * @num2: the second number
 * Return: the difference of two number
 */

int sub(int num1, int num2)
{
	return (num1 - num2);
}

/**
 * mul - returns the multiplication of two numbers
 * @num1: the first number
 * @num2: the second number
 * Return: multiplication of thw two numbers
 */

int mul(int num1, int num2)
{
	return (num1 * num2);
}

/**
 * div - returns the division of two numbers
 * @num1: the first number
 * @num2: the second number
 * Return: the division of the two numbers
 */

int div(int num1, int num2)
{
	return (num1 / num2);
}

/**
 * mod - returns modulus of two numbers
 * @num1: the first number
 * @num2: the second number
 * Return: the modulus of the two number
 */

int mod(int num1, int num2)
{
	return (num1 % num2);
}
